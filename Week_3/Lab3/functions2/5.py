def average(movies):
   d = dict()
   for movie in movies:
      if not movie['category'] in d:
         d[movie['category']] = movie['imdb']
      else:
         d[movie['category']] += movie['imdb']
   return d

def cnt(movies):
   d2 = dict()
   for movie in movies:
      if not movie['category'] in d2:
         d2[movie['category']] = 1
      else:
         d2[movie['category']] += 1
   return d2


